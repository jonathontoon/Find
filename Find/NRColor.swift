    func domainrBackgroundGreyColor() -> UIColor {
        return UIColor(CSS: "F9FAF9")
    }
    
    func domainrSubtextGreyColor() -> UIColor {
        return UIColor(CSS: "949693")
    }
    
    func domainrListIconGreyColor() -> UIColor {
        return UIColor(CSS: "E0E6DF")
    }
    
    func domainrBlueColor() -> UIColor {
        return UIColor(CSS: "2870B0")
    }
    
    func domainrGreenColor() -> UIColor {
        return UIColor(CSS: "23B000")
    }
    
    func domainrRedColor() -> UIColor {
        return UIColor(CSS: "FF4D00")
    }
    
    func domainrOrangeColor() -> UIColor {
        return UIColor(CSS: "F99400")
    }

}
